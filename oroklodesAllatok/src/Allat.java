class Allat {
    private String faj;
    private int eletkor;

    public Allat(String faj, int eletkor) {
        this.faj = faj;
        this.eletkor = eletkor;
    }

    public String getFaj() {
        return faj;
    }

    public void setFaj(String faj) {
        this.faj = faj;
    }

    public int getEletkor() {
        return eletkor;
    }

    public void setEletkor(int eletkor) {
        this.eletkor = eletkor;
    }
}