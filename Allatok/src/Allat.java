public class Allat {
    private String nev;
    private String fajta;
    private int elettartam;
    private String szin;
    private String kultakaro;
    private int suly;

    public String getNev() {
        return nev;
    }

    public void setNev(String nev) {
        this.nev = nev;
    }

    public String getFajta() {
        return fajta;
    }

    public void setFajta(String fajta) {
        this.fajta = fajta;
    }

    public int getElettartam() {
        return elettartam;
    }

    public void setElettartam(int elettartam) {
        this.elettartam = elettartam;
    }

    public String getSzin() {
        return szin;
    }

    public void setSzin(String szin) {
        this.szin = szin;
    }

    public String getKultakaro() {
        return kultakaro;
    }

    public void setKultakaro(String kultakaro) {
        this.kultakaro = kultakaro;
    }

    public int getSuly() {
        return suly;
    }

    public void setSuly(int suly) {
        this.suly = suly;
    }
}